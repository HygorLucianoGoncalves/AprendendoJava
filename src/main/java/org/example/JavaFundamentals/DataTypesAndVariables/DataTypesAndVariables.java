package org.example.JavaFundamentals.DataTypesAndVariables;

public class DataTypesAndVariables {
    public static void main(String[] args) {

        /* TIPOS PRIMITIVOS */

        //TIPOS LÔGICOS
        boolean verdadeiro = true; // or false

        //LITERAIS
        char umChar = 'a';

        //INTEIROS
        byte oneByte = 127;
        short oneShort = 32767;
        int oneInt = 32767;
        long oneLong = 2147483;

        //REAIS
        float oneFloat = 3.6f;
        double oneDoble = 1.8;

        //Não Primitivo - String, Arrays etc

        String nome = "Mario";

        //Arrays
        String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};

        System.out.println(cars[0]);
    }
}
